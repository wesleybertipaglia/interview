# challenge-title: Flipping the Matrix

# Description:
# Sean invented a game involving a 2n x 2n matrix where each cell of the matrix contains an integer.
# He can reverse any of its rows or columns any number of times, and the goal of the game is to maximize the sum of the elements in the n x n submatrix located in the upper-left quadrant of the matrix.
# Given the initial configurations for q matrices, help Sean reverse the rows and columns of each matrix in the best possible way so that the sum of the elements in the matrix's upper-left quadrant is maximal.

# Example:
# matrix = [[1,2], [3,4]]
# The initial matrix looks like this:
# 1 2
# 3 4

# Its a 2x2 matrix, so we want to maximize the sum of the upper-left quadrant. The submatrix we care about is:
# 1 2
# 3 4

# and now reverse column:
# 4 2
# 1 3

# The maximum sum is 4

